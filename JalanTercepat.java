    static void temukanJalan(char[][] map) {
        int a = map.length;
        int b = map[0].length;
        boolean[][] visited = new boolean[a][b];
        Point start = null, end = null;

        for (int i = 0; i < a; i++) {
            for (int j = 0; j < b; j++) {
                if (map[i][j] == '^') {
                    start = new Point(i, j, 0, "");
                } else if (map[i][j] == '*') {
                    end = new Point(i, j, 0, "");
                }
            }
        }

        if (start == null || end == null) {
            System.out.println("tidak ada jalan");
            return;
        }

        Queue<Point> queue = new LinkedList<>();
        queue.add(start);
        visited[start.x][start.y] = true;

        while (!queue.isEmpty()) {
            Point p = queue.poll();

            if (p.x == end.x && p.y == end.y) {
                System.out.println(p.path);
                System.out.println(p.dist + " langkah");
                return;
            }

            for (int i = 0; i < 4; i++) {
                int nx = p.x + Movement.ax[i];
                int ny = p.y + Movement.ay[i];

                if (isValid(nx, ny, a, b, map, visited)) {
                    visited[nx][ny] = true;
                    queue.add(new Point(nx, ny, p.dist + 1, p.path + (p.dist == 0 ? "" : ", ") + "1 " + Movement.arah[i]));
                }
            }
        }

        System.out.println("tidak ada jalan");
    }

    static boolean isValid(int x, int y, int n, int m, char[][] map, boolean[][] visited) {
        return x >= 0 && y >= 0 && x < n && y < m && map[x][y] != '#' && !visited[x][y];
    }
